<?php
    // Assignment Operators
    // =, +=, -=, *=, /=, %=, **=, .=

    $a = 10;
    $b = 2;

    $a += $b;
    echo $a;
    echo "<br>";

    $a -= $b;
    echo $a;
    echo "<br>";

    $a *= $b;
    echo $a;
    echo "<br>";

    $a /= $b;
    echo $a;
    echo "<br>";

    $a %= $b;
    echo $a;
    echo "<br>";

    $a **= $b;
    echo $a;
    echo "<br>";

    $a .= $b;
    echo $a;
    echo "<br>";

    
?>